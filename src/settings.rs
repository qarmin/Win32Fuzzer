#[derive(Eq, PartialEq, Clone, Copy, Ord, PartialOrd)]
pub enum TypeOfProblem {
    NotImplementedLinux,
    NotImplementedWindows,
    InvalidNumberOfArguments,
    ShowsDialogWindows,
    ShowsDialogLinux,
    CrashesWindows,
    CrashesLinux,
    CrashAutomatic, // Crashes are normal for this code
    Freeze,
    Other,
    Mismatch3264BitFunctions, // 32 and 64 bit functions have different definition and currently are not handled
}
impl TypeOfProblem {
    pub fn to_string(self) -> String {
        match self {
            NotImplementedLinux => "NotImplementedLinux",
            NotImplementedWindows => "NotImplementedWindows",
            InvalidNumberOfArguments => "InvalidNumberOfArguments",
            ShowsDialogWindows => "ShowsDialogWindows",
            ShowsDialogLinux => "ShowsDialogLinux",
            CrashesWindows => "CrashesWindows",
            CrashesLinux => "CrashesLinux",
            CrashAutomatic => "CrashAutomatic",
            Freeze => "Freeze",
            Other => "Other",
            Mismatch3264BitFunctions => "Mismatch3264BitFunctions",
        }
        .to_string()
    }
}

